// 数値の比較演算
let a = 10
let b = 10
let result = a == b

// 比較演算子
// ==→右辺と左辺が一致する
// !=→右辺と左辺が一致しない
// >　→右辺が左辺より小さい
// >=　→右辺が左辺以下
// < →右辺が左辺より大きい
// <= →右辺が左辺以上

let c = 100 != 100

let d = 100 > 100
let e = 100 >= 100

let f = 100 < 100
let g = 100 <= 100

let h = 1.5 == 1.5
let i = 1.5 != 1.5
let j = 1.5 < 1.5
let k = 1.5 <= 1.5
print(h)
print(i)
print(j)
print(k)

